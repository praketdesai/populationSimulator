#ifndef FIGHTING_H
#define FIGHTING_H

class Fighting {

    /**
     * Damage of a flurry of basic punches
     * @return Double
     */
    double basicPunches() {};

    /**
     * Damage of a martial art knowledged attack
     * @return Double
     */
    double martialArtistry() {};

    /**
     * Flaming fists, deals melee fire damage and applies burning to target opponent
     * @return Double
     */
    double flamingFists() {};

    /**
     * Fire blast, deals ranged fire damage and could apply burning to target opponent
     * @return Double
     */
    double fireBlast() {};

    /**
     * Rock Shield, creates a shield that can tank any type of damage
     * @return Double
     */
    double rockShield() {};

    /**
     * Rock Blast, deals ranged earth damage to the enemy
     * @return Double
     */
    double rockShield() {};

};


#endif